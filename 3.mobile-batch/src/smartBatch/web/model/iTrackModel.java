package smartBatch.web.model;

public class iTrackModel {
	String panelid;
	String rowid;
	String domain;
	String ext_domain;
	int siteid;
	int p30;
	int pv_adjust;
	
	public String getPanelid() {
		return panelid;
	}
	public void setPanelid(String panelid) {
		this.panelid = panelid;
	}
	public String getRowid() {
		return rowid;
	}
	public void setRowid(String rowid) {
		this.rowid = rowid;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getExt_domain() {
		return ext_domain;
	}
	public void setExt_domain(String ext_domain) {
		this.ext_domain = ext_domain;
	}
	public int getSiteid() {
		return siteid;
	}
	public void setSiteid(int siteid) {
		this.siteid = siteid;
	}
	public int getP30() {
		return p30;
	}
	public void setP30(int p30) {
		this.p30 = p30;
	}
	public int getPv_adjust() {
		return pv_adjust;
	}
	public void setPv_adjust(int pv_adjust) {
		this.pv_adjust = pv_adjust;
	}
}
